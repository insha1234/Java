package Strings;

public class prettyPrinting {
    public static void main(String[] args) {
        float a = 10.1234f;
        double b = 20.0;
        System.out.printf("The value of a is %.2f and the value of b is %f", a, b);

        System.out.println();

        System.out.printf("The value of pi is: %.2f" , Math.PI);
    }
    
}
